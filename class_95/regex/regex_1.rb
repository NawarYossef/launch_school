
# Write a regex that matches any text that contains an uppercase K. Test it with these strings:
string =  " Kx BlacK kelly"
# solution : 
/hz/

# Write a regex that matches any text that contains an uppercase or lowercase H. Test it with these strings:
string = "Henry perch golf"
# solution : 
/h/i

# Write a regex that matches the string "dragon". Test it with these strings:
string = "snapdragon bearded dragon dragoon"
# solution : 
/dragon/

# Write a regex that matches any of the following fruits: banana, orange, apple, strawberry. Test it with these strings:
string = "banana orange pineapples strawberry raspberry"
# Solution : 
/(orange|banana|apple|strawberry)/

# Write a regex that matches a comma or a space. Test your regex with these strings:
string = "This line has spaces This,line,has,commas, No-spaces-or-commas"
# Solution : 
/( |,)/

# Challenge: Write a regex that matches "blueberry" or "blackberry", but do so by writing berry only once. Test it with these strings:
string = "blueberry blackberry black berry strawberry"
# Solution : 
/(blue|black)berry/

